package com.example.quickdoc;

public class User {

    public  String email;
    public  String name;
    public  String phone;
    public  String dob;
    public  String gender;


    public User(){

    }

    public User(String email, String name, String phone, String dob, String gender){

        this.email=email;
        this.name=name;
        this.phone=phone;
        this.dob=dob;
        this.gender=gender;
    }
}
